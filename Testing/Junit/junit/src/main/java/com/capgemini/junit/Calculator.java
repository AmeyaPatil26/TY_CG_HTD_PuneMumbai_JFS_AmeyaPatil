package com.capgemini.junit;

public class Calculator {

	public int add(int a, int b) {
		return a + b;
	}//End of add()

	public int sub(int a, int b) {
		return a - b;
	}//End of sub()
	
	public int div(int a, int b) {
		return a/b;
	}//End of div()
	public int mul(int a,int b) {
		return a*b;
	}//end of mul()
	
	public int mod(int a, int b) {
		return a%b;
	}//End of mod()
}//End of class
